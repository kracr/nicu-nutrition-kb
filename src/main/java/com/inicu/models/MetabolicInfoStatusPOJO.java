package com.inicu.models;

public class MetabolicInfoStatusPOJO {
	
	String hypoglycemiaStatus;
	String hyponatremiaStatus;
	String hypocalcemiaStatus;
	String hypokalemiaStatus;
	String AcidosisStatus;
	
	
	public String getHypoglycemiaStatus() {
		return hypoglycemiaStatus;
	}
	public void setHypoglycemiaStatus(String hypoglycemiaStatus) {
		this.hypoglycemiaStatus = hypoglycemiaStatus;
	}
	public String getHyponatremiaStatus() {
		return hyponatremiaStatus;
	}
	public void setHyponatremiaStatus(String hyponatremiaStatus) {
		this.hyponatremiaStatus = hyponatremiaStatus;
	}
	public String getHypocalcemiaStatus() {
		return hypocalcemiaStatus;
	}
	public void setHypocalcemiaStatus(String hypocalcemiaStatus) {
		this.hypocalcemiaStatus = hypocalcemiaStatus;
	}
	public String getHypokalemiaStatus() {
		return hypokalemiaStatus;
	}
	public void setHypokalemiaStatus(String hypokalemiaStatus) {
		this.hypokalemiaStatus = hypokalemiaStatus;
	}
	public String getAcidosisStatus() {
		return AcidosisStatus;
	}
	public void setAcidosisStatus(String acidosisStatus) {
		AcidosisStatus = acidosisStatus;
	}
	
	

}
