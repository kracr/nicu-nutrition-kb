package com.inicu.models;

public class IdentificationValue {
	
	private String value;
	private String colorIdentification;
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getColorIdentification() {
		return colorIdentification;
	}
	public void setColorIdentification(String colorIdentification) {
		this.colorIdentification = colorIdentification;
	}

}
